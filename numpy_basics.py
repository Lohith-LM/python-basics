import numpy as np

# Create a NumPy array
arr = np.array([10, 20, 30, 40, 50])

print("Array:", arr)
print("First element:", arr[0])
print("Last element:", arr[-1])

# Basic operations
print("Add 5 to each element:", arr + 5)
print("Multiply each element by 2:", arr * 2)

# Array shape
print("Shape of array:", arr.shape)
